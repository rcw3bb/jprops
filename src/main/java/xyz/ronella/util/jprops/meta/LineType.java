package xyz.ronella.util.jprops.meta;

/**
 * The LineType enum is an enumeration of the line types.
 *
 * @author Ron Webb
 * @since 1.0.0
 */
public enum LineType {
    /**
     * The line type is a key value pair.
     */
    VALUE_PAIR,

    /**
     * The line type is a comment.
     */
    COMMENT,

    /**
     * The line type is just any text
     */
    TEXT
}
